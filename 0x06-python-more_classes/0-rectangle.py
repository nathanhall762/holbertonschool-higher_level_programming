#!/usr/bin/python3
class Rectangle:
    def __init__(self, width=0, height=0):
        self.height = height
        self.width = width

    def height(self):
        return self.height

    def height(self, value):
        self.height = value

    def width(self):
        return self.width

    def width(self, value):
        self.width = value
